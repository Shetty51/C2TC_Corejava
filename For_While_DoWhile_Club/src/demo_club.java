public class demo_club {

	
	public static void main(String[] args) {
		
		
		 // for loop
		  for(int i=1,j=1; i<10||j<10;i++,j++)
		  { 
			  j++;
			  System.out.println("i="+i+" and j="+j); 
		  }


		  // while loop 
			  int a=1,b=1;
			  while(a<10 || b<10)
			  { 
				  a++; b++;
			      System.out.println("a="+a+" and b="+b); 
			  }	
			
		// Do while loop
			     int m = 1, n = 1;
			do {
				m++;
				n++;
				System.out.println("m=" + m + " and n=" + n);
			    } while (m < 10 || n < 10);
		}
	

	}


